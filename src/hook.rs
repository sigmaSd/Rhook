use crate::libcfn;

macro_rules! gen_hook_enum {
    ($($variant: ident)*) => (
        #[allow(non_camel_case_types)]
        #[non_exhaustive]
        #[derive(Debug, Hash, Eq, PartialEq)]
        /// libc hooks enum
        pub enum Hook {
            $($variant(&'static str),)*
        }
        impl Hook {
            pub fn function(&self, ) -> String {
                match self {
                    $(Hook::$variant(fun) => libcfn::$variant(fun),)* 
                }
            }
        }
    )
}


